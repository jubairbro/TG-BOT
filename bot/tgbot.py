def get_bot_uptime():
    """Calculates bot's uptime."""
    uptime_seconds = time.time() - BOT_START_TIME
    d, rem = divmod(uptime_seconds, 86400)
    h, rem = divmod(rem, 3600)
    m, s = divmod(rem, 60)
    return f"{int(d)}d {int(h)}h {int(m)}m {int(s)}s"

